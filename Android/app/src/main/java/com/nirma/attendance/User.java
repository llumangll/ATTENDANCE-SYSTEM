package com.nirma.attendance;

public class User {
    private String uid;
    private String name;
    private String role;
    private String department;

    // Empty Constructor
    public User() {}

    // Getters
    public String getUid() { return uid; }
    public String getName() { return name; }
    public String getRole() { return role; }
    public String getDepartment() { return department; }
}